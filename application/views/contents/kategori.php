            <div class="modal-body">
                <p>
                    Menurut Wikipedia “Arsitektur adalah seni atau praktik merancang dan membangun bangunan”. Analis pasar TI Gartner mendefinisikan arsitektur TI sebagai “serangkaian prinsip, pedoman, atau aturan yang digunakan oleh perusahaan untuk mengarahkan proses memperoleh, membangun, memodifikasi, dan menghubungkan sumber daya TI di seluruh perusahaan.”

                    Dan keduanya benar. Di satu sisi arsitektur TI adalah proses merancang dan membangun solusi TI yang dipandu oleh prinsip dan pedoman. Tapi tetap saja definisi tersebut tidak memiliki apa yang seharusnya mencakup penyampaian arsitektur yang terdefinisi dengan baik.
                </p>
                <p>
                <h5><b>Arsitektur TI harus mencakup dan memfasilitasi hal-hal berikut:</b></h5>
                Merupakan diagram tingkat tinggi dari komponen TI dalam suatu aplikasi, meliputi desain perangkat lunak dan perangkat keras,<br>
                Menunjukkan dasar-dasar bagaimana solusi yang dibuat khusus atau produk vendor dirancang dan dibangun untuk diintegrasikan dengan sistem yang ada dan memenuhi persyaratan tertentu,<br>

                Terintegrasi dalam metodologi pengembangan perangkat lunak untuk memahami dan merancang spesifikasi dan model perangkat lunak dan perangkat keras TI sesuai dengan standar, pedoman, dan spesifikasi,<br>
                Memanfaatkan Praktik Terbaik untuk mendorong penggunaan standar teknologi (misalnya “terbuka”), interoperabilitas teknologi global, dan platform TI yang ada (integrasi, data, dll.),<br>

                Menyediakan cara yang konsisten, koheren, dan universal untuk menunjukkan dan mendiskusikan desain dan penyampaian kemampuan TI solusi.<br>
                </p>
                <p>
                <h5><b>Jenis Arsitektur Teknologi Informasi</b></h5>
                Hal lain yang membingungkan orang adalah ada berbagai jenis arsitektur TI. Faktanya adalah seperti ketika berbicara tentang mendesain bangunan sebagai contoh paralel, ada tingkat arsitektur yang berbeda seperti arsitektur keseluruhan bangunan, denah lantai untuk setiap lantai, dan desain interior. Hal yang sama berlaku untuk arsitektur TI dan kita dapat membedakan tiga jenis Arsitektur TI:<br>
                - Enterprise Arsitektur (EA)<br>
                - Solution Architecture (SA)<br>
                - Technology Architecture (juga disebut secara umum sebagai Arsitektur TI)
                </p>
